#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	p;
	size_t	i;

	i = 0;
	p = ft_strlen(s1);
	while (i != n && s2[i] != '\0')
	{
		s1[p] = s2[i];
		p++;
		i++;
	}
	s1[p] = '\0';
	return (s1);
}
