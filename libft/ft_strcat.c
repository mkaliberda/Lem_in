#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t n;
	size_t i;

	i = 0;
	n = ft_strlen(s1);
	while (s2[i] != '\0')
	{
		s1[n] = s2[i];
		n++;
		i++;
	}
	s1[n] = '\0';
	return (s1);
}
